    <script>
        const services = [
            { icon: 'üåê', label: 'Web Development', link: '#' },
            { icon: 'üì±', label: 'Mobile App Solutions', link: '#' },
            { icon: 'üìä', label: 'Real Estate CRM', link: '#' },
            { icon: 'üìí', label: 'Khata Book App', link: '#' },
            { icon: 'üì©', label: 'Get a Free Quote', link: 'https://wa.me/917208344434' }
        ];

        let isTyping = false;

        function toggleChat() {
            const window = document.getElementById('chatWindow');
            const isHidden = window.classList.contains('hidden');
            
            if (isHidden) {
                window.classList.remove('hidden');
                startChatSequence();
            } else {
                window.classList.add('hidden');
                resetChat();
            }
        }

        async function startChatSequence() {
            if (isTyping) return;
            isTyping = true;
            
            const textElement = document.getElementById('typingText');
            const message = "Hello! üëã RudraTech Agent here. How can we help build your vision today?";
            textElement.innerHTML = "";
            
            // 1. Typing Effect
            for (let i = 0; i < message.length; i++) {
                textElement.innerHTML += message.charAt(i);
                await new Promise(r => setTimeout(r, 30)); // Speed of typing
            }
            document.getElementById('cursor').classList.add('hidden');

            // 2. Show Service List
            const list = document.getElementById('serviceList');
            list.classList.remove('hidden');

            // 3. One by one service appearance
            for (let i = 0; i < services.length; i++) {
                const item = document.createElement('a');
                item.href = services[i].link;
                item.className = `animate-item flex items-center gap-3 p-3 bg-white border border-gray-100 rounded-xl hover:bg-blue-50 hover:border-blue-200 transition-all shadow-sm group`;
                item.style.animationDelay = `${i * 0.15}s`;
                
                item.innerHTML = `
                    <span class="text-lg">${services[i].icon}</span>
                    <span class="text-sm font-semibold text-gray-700 group-hover:text-blue-600">${services[i].label}</span>
                `;
                list.appendChild(item);
                await new Promise(r => setTimeout(r, 150)); 
            }
        }

        function resetChat() {
            document.getElementById('typingText').innerHTML = "";
            document.getElementById('serviceList').innerHTML = "";
            document.getElementById('serviceList').classList.add('hidden');
            document.getElementById('cursor').classList.remove('hidden');
            isTyping = false;
        }
    </script>
